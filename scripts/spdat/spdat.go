func DurationCalc(calc int, level int) int {
	switch calc {
	case 1:
		if level > 3 {
			return level / 2
		}
		return 1
	case 2:
		if level > 3 {
			return level/2 + 5
		}
		return 6
	case 3:
		return 30 * level
	case 4:
		return 50
	case 5:
		return 2
	case 6:
		if level > 1 {
			return level/2 + 2
		}
		return 1
	case 7:
		return level
	case 8:
		return level + 10
	case 9:
		return 2*level + 10
	case 10:
		return 3*level + 10
	case 11:
		return 30 * (level + 3)
	case 12:
		if level > 7 {
			return level / 4
		}
		return 1
	case 13:
		return 4*level + 10
	case 14:
		return 5 * (level + 2)
	case 15:
		return 10 * (level + 10)
	case 50:
		return -1
	case 51:
		return -4
	default:
		if calc < 200 {
			return 0
		}
	}
	return calc
}

